let n = 10;
let m = 5;

let operator = "+";   // you can change this to ("-", "*", "/")

if (operator === "+") {
    console.log( n + m)
}
else if (operator === "-") {
    console.log( n-m)
}
else if (operator === "*") {
    console.log( n*m)
}
else if (operator === "/") {
    console.log( n/m)
}
else{
    console.log( "Enter a Valid Operator")
}
